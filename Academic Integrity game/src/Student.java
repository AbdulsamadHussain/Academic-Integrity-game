public class Student {
	String userName;
	int BOO;

	public Student ( String userName , int BOO ) {
		this.userName = userName;
		this.BOO = BOO;
	}

	public String getUserName () {
		return userName;
	}

	public void setUserName ( String userName ) {
		this.userName = userName;
	}

	public int getBOO () {
		return BOO;
	}

	public void setBOO ( int BOO ) {
		this.BOO = BOO;
	}
}
